/*
 * reverseBytes - reverse the bytes of x
 *   Example: reverseBytes(0x01020304) = 0x04030201
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 25
 *   Rating: 3
 */
int reverseBytes(int x) {
  /* Used | operator with four separate masks within the 0xff range */

  int a = x & 0xff;
  int b = (x >> 8) & 0xff;
  int c = (x >> 16) & 0xff;
  int d = (x >> 24) & 0xff;

  return a << 24 | b << 16 | c << 8 | d;
}
/*
 * logicalShift - shift x to the right by n, using a logical shift
 *   Can assume that 1 <= n <= 31
 *   Examples: logicalShift(0x87654321,4) = 0x08765432
 *   Legal ops: ~ & ^ | + << >>
 *   Max ops: 16
 *   Rating: 3
 */
int logicalShift(int x, int n) {
  /* Used a dynamic mask that allows for arithmetically shifted bits to be converted to logical with & operator */

  return (x >> n) & (~(((1 << 31) >> n) << 1));;
}
/*
 * isGreater - if x > y  then return 1, else return 0
 *   Example: isGreater(4,5) = 0, isGreater(5,4) = 1
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 24
 *   Rating: 3
 */
int isGreater(int x, int y) {
  /* Used | operator with masks of x and y compared with each other and converted to boolean with ! operator */

  int mask_x = x >> 31;
  int mask_y = y >> 31;
  int equal = (!(mask_x ^ mask_y)) & ((~y + x) >> 31);
  int notEqual = mask_x & ! mask_y;

  return !( equal | notEqual);
}
/*
 * bitMask - Generate a mask consisting of all 1's
 *   lowbit and highbit
 *   Examples: bitMask(5,3) = 0x38
 *   Assume 0 <= lowbit <= 31, and 0 <= highbit <= 31
 *   If lowbit > highbit, then mask should be all 0's
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 16
 *   Rating: 3
 */
int bitMask(int highbit, int lowbit) {
  /* Flips the bits of x used against y with | operator with the ~ operator */

  int x = (~0 << highbit);
  int y = ~(~0 << lowbit);
  int z = ~(1 << highbit);

  x = x & z;

  return ~(x | y);
}
