/*
 * abs - absolute value of x (except returns TMin for TMin)
 *   Example: abs(-1) = 1.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 10
 *   Rating: 4
 */
int abs(int x) {
  /* Used | operator with masks compared with & operators */

  return ((~x >> 31) & x) | ((x >> 31) & ((~x) + 1));
}
/*
 * bitCount - returns count of number of 1's in word
 *   Examples: bitCount(5) = 2, bitCount(7) = 3
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 40
 *   Rating: 4
 */
int bitCount(int x) {
  /* Used two masks to add sums of x shifted right */

  int mask1 = 0x11 | (0x11 << 8);
  int mask2 = mask1 | (mask1 << 16);

  int sum = x & mask2;

  sum = sum + ((x >> 1) & mask2);
  sum = sum + ((x >> 2) & mask2);
  sum = sum + ((x >> 3) & mask2);
  sum = sum + (sum >> 16);
  mask1 = 0xF | (0xF << 8);
  sum = (sum & mask1) + ((sum >> 4) & mask1);

  return ((sum + (sum >> 8)) & 0x3F);
}
/*
 * isNonZero - Check whether x is nonzero using
 *              the legal operators except !
 *   Examples: isNonZero(3) = 1, isNonZero(0) = 0
 *   Legal ops: ~ & ^ | + << >>
 *   Max ops: 10
 *   Rating: 4
 */
int isNonZero(int x) {
  /* Used | operator with shifted sign bits and a mask against 1 */

  return (((~x + 1) >> 31) | (x >> 31)) & 1;
}
/*
 * bang - Compute !x without using !
 *   Examples: bang(3) = 0, bang(0) = 1
 *   Legal ops: ~ & ^ | + << >>
 *   Max ops: 12
 *   Rating: 4
 */
int bang(int x) {
  /* Shifted x every power of 2 and then compared with | operator against x */

  x = ( x >> 16 ) | x;
  x = ( x >> 8 ) | x;
  x = ( x >> 4 ) | x;
  x = ( x >> 2 ) | x;
  x = ( x >> 1) | x;

  return ~x & 1;
}
